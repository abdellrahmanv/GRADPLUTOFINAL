# Cache loaded models to avoid reloading between trials
_openai_whisper_models = {}
_faster_whisper_models = {}


def load_openai_whisper(model_name):
    """Load an OpenAI Whisper model (PyTorch)"""
    if model_name in _openai_whisper_models:
        return _openai_whisper_models[model_name]

    import whisper
    print(f"    Loading OpenAI Whisper '{model_name}' (PyTorch, FP32)...")
    t0 = time.perf_counter()
    model = whisper.load_model(model_name)
    load_time = (time.perf_counter() - t0) * 1000
    print(f"    Loaded in {load_time:.0f}ms")

    # Warmup
    silence = np.zeros(16000, dtype=np.float32)
    model.transcribe(silence, language="en", fp16=False)

    _openai_whisper_models[model_name] = model
    return model


def load_faster_whisper(model_name, compute_type):
    """Load a faster-whisper model (CTranslate2)"""
    key = f"{model_name}_{compute_type}"
    if key in _faster_whisper_models:
        return _faster_whisper_models[key]

    from faster_whisper import WhisperModel
    cache_dir = os.path.expanduser("~/.cache/whisper")
    os.makedirs(cache_dir, exist_ok=True)

    print(f"    Loading faster-whisper '{model_name}' ({compute_type})...")
    t0 = time.perf_counter()
    model = WhisperModel(model_name, device="cpu", compute_type=compute_type,
                         download_root=cache_dir)
    load_time = (time.perf_counter() - t0) * 1000
    print(f"    Loaded in {load_time:.0f}ms")

    # Warmup
    silence = np.zeros(16000, dtype=np.float32)
    segs, _ = model.transcribe(silence, language="en", beam_size=1)
    list(segs)

    _faster_whisper_models[key] = model
    return model


def benchmark_stt_openai(model, audio_float, beam_size=5):
    """Run OpenAI Whisper transcription and return (text, latency_ms)"""
    import whisper

    start = time.perf_counter()
    # OpenAI Whisper expects a file path or padded audio array
    # Pad/trim to 30s as Whisper expects
    audio_padded = whisper.pad_or_trim(
        torch.from_numpy(audio_float).float()
    ).numpy() if hasattr(audio_float, '__len__') else audio_float

    # Actually, let's use the simpler transcribe API with a temp file
    # because whisper.transcribe() handles the audio loading
    # But for timing accuracy, we'll work directly with the model

    # Use the transcribe function with numpy audio
    result = model.transcribe(
        audio_float,
        language="en",
        fp16=False,  # FP32 on CPU (no CUDA)
        beam_size=beam_size,
        without_timestamps=True,
    )
    latency = (time.perf_counter() - start) * 1000
    text = result["text"].strip()
    return text, latency


def benchmark_stt_faster(model, audio_float, beam_size=1):
    """Run faster-whisper transcription and return (text, latency_ms)"""
    start = time.perf_counter()
    segments, info = model.transcribe(
        audio_float,
        language="en",
        beam_size=beam_size,
        best_of=1,
        vad_filter=False,
        condition_on_previous_text=False,
        without_timestamps=True,
    )
    text = "".join(seg.text for seg in segments).strip()
    latency = (time.perf_counter() - start) * 1000
    return text, latency


