def run_version(version_id, config, audio_float, num_trials, queries):
    """Run all trials for one version. Returns list of trial results."""
    print(f"\n{'='*60}")
    print(f"  {config['name']}")
    print(f"{'='*60}")

    # --- Load STT model ---
    stt_model = None
    if config["stt_engine"] == "openai-whisper":
        try:
            import torch  # needed for OpenAI Whisper
            # Quick sanity check — catches "Illegal instruction" on RPi4
            torch.zeros(1)
            stt_model = load_openai_whisper(config["stt_model"])
        except ImportError:
            print("    ❌ openai-whisper or PyTorch not installed. Skipping this version.")
            print("       Install: pip install openai-whisper")
            return None
        except Exception as e:
            print(f"    ❌ PyTorch/Whisper failed: {e}")
            print("       This often happens on RPi4 (Cortex-A72) because PyPI PyTorch")
            print("       wheels use ARM instructions not available on this CPU.")
            print("       Skipping this version. Use --skip-openai to avoid this message.")
            return None
    elif config["stt_engine"] == "faster-whisper":
        try:
            stt_model = load_faster_whisper(config["stt_model"], config["stt_compute"])
        except ImportError:
            print("    ❌ faster-whisper not installed. Skipping this version.")
            print("       Install: pip install faster-whisper")
            return None

    # --- Warmup LLM ---
    if config["llm_enabled"]:
        warmup_llm(config["llm_model"])

    # --- Run trials ---
    trials = []
    for t in range(1, num_trials + 1):
        print(f"\n  Trial {t}/{num_trials}")
        trial = {"trial": t, "version": version_id}

        # 1. STT
        if config["stt_engine"] == "openai-whisper":
            text, stt_ms = benchmark_stt_openai(stt_model, audio_float,
                                                 beam_size=config["stt_beam"])
        else:
            text, stt_ms = benchmark_stt_faster(stt_model, audio_float,
                                                 beam_size=config["stt_beam"])

        trial["stt_ms"] = round(stt_ms, 1)
        trial["stt_text"] = text
        print(f"    STT: \"{text}\" — {stt_ms:.0f}ms")

        if not text:
            text = "What is the weather like today?"  # fallback for empty transcription

        # 2. LLM (or keyword matching)
        if config["llm_enabled"]:
            query = queries[(t - 1) % len(queries)]
            # Use the transcribed text for STT realism, but fall back to known query
            llm_input = text if len(text) > 3 else query

            response, llm_ms, ft_ms = benchmark_llm(llm_input, config)
            trial["llm_ms"] = round(llm_ms, 1)
            trial["llm_first_token_ms"] = round(ft_ms, 1)
            trial["llm_response"] = response or ""
            print(f"    LLM: \"{(response or '')[:60]}\" — {llm_ms:.0f}ms (1st tok {ft_ms:.0f}ms)")

            tts_text = response or "I'm not sure how to answer that."
        else:
            # V1/V2: keyword matching — instant
            trial["llm_ms"] = 0.0
            trial["llm_first_token_ms"] = 0.0
            trial["llm_response"] = "Hey, I'm Pluto — an AI-powered welcoming robot!"
            tts_text = trial["llm_response"]
            print(f"    LLM: (keyword match) — 0ms")

        # 3. TTS
        wav_path, tts_ms = benchmark_tts(tts_text)
        trial["tts_ms"] = round(tts_ms, 1)
        print(f"    TTS: {tts_ms:.0f}ms")

        # Cleanup TTS wav
        if wav_path:
            try:
                os.unlink(wav_path)
            except:
                pass

        # 4. Total
        total = trial["stt_ms"] + trial["llm_ms"] + trial["tts_ms"]
        trial["total_ms"] = round(total, 1)
        print(f"    TOTAL: {total:.0f}ms  (STT {trial['stt_ms']:.0f} + LLM {trial['llm_ms']:.0f} + TTS {trial['tts_ms']:.0f})")

        trials.append(trial)

        # Brief pause between trials
        time.sleep(0.3)

    # --- Summary ---
    stt_vals = [t["stt_ms"] for t in trials]
    llm_vals = [t["llm_ms"] for t in trials]
    tts_vals = [t["tts_ms"] for t in trials]
    total_vals = [t["total_ms"] for t in trials]

    summary = {
        "version": version_id,
        "name": config["name"],
        "config": {k: v for k, v in config.items() if k != "name"},
        "n_trials": len(trials),
        "stt_mean": round(float(np.mean(stt_vals)), 1),
        "stt_std": round(float(np.std(stt_vals)), 1),
        "llm_mean": round(float(np.mean(llm_vals)), 1),
        "llm_std": round(float(np.std(llm_vals)), 1),
        "tts_mean": round(float(np.mean(tts_vals)), 1),
        "tts_std": round(float(np.std(tts_vals)), 1),
        "total_mean": round(float(np.mean(total_vals)), 1),
        "total_std": round(float(np.std(total_vals)), 1),
        "trials": trials,
    }

    if config["llm_enabled"]:
        ft_vals = [t["llm_first_token_ms"] for t in trials]
        summary["llm_ft_mean"] = round(float(np.mean(ft_vals)), 1)
        summary["llm_ft_std"] = round(float(np.std(ft_vals)), 1)

    print(f"\n  --- {version_id} Summary (N={len(trials)}) ---")
    print(f"  STT:   {summary['stt_mean']:8.1f} ± {summary['stt_std']:.1f} ms")
    print(f"  LLM:   {summary['llm_mean']:8.1f} ± {summary['llm_std']:.1f} ms")
    print(f"  TTS:   {summary['tts_mean']:8.1f} ± {summary['tts_std']:.1f} ms")
    print(f"  TOTAL: {summary['total_mean']:8.1f} ± {summary['total_std']:.1f} ms")

    return summary


def main():
    parser = argparse.ArgumentParser(description="Timeline Benchmark — All Versions")
    parser.add_argument("--trials", type=int, default=10,
                        help="Trials per version (default: 10)")
    parser.add_argument("--versions", nargs="+", default=None,
                        help="Specific versions to test (e.g. V1 V4). Default: all")
    parser.add_argument("--skip-openai", action="store_true",
                        help="Skip V1/V2/V3 (requires openai-whisper/PyTorch)")
    parser.add_argument("--audio", type=str, default=None,
                        help="Path to test WAV file (default: test_audio.wav)")
    args = parser.parse_args()

    print("=" * 60)
    print("  TIMELINE BENCHMARK — All Voice Assistant Versions")
    print(f"  Trials per version: {args.trials}")
    print("=" * 60)

    # Determine which versions to test
    if args.versions:
        version_ids = [v.upper().replace("-", "-") for v in args.versions]
        # Normalize: allow "v1" -> "V1", "v3opt" -> "V3-opt", etc.
        normalized = []
        for v in args.versions:
            v_up = v.upper()
            if v_up in VERSIONS:
                normalized.append(v_up)
            elif v_up == "V3OPT" or v_up == "V3-OPT":
                normalized.append("V3-opt")
            elif v_up.replace("-", "-") in VERSIONS:
                normalized.append(v_up)
            else:
                print(f"  ⚠️ Unknown version: {v}. Available: {list(VERSIONS.keys())}")
        version_ids = normalized
    elif args.skip_openai:
        version_ids = ["V3-opt", "V4"]
    else:
        version_ids = list(VERSIONS.keys())

    print(f"  Testing: {', '.join(version_ids)}")

    # Load or generate test audio
    audio_path = Path(args.audio) if args.audio else TEST_AUDIO
    if not audio_path.exists():
        if not generate_test_audio():
            sys.exit(1)
        audio_path = TEST_AUDIO

    audio_float = load_audio_as_float(audio_path)
    print(f"  Test audio: {audio_path} ({len(audio_float)/16000:.1f}s)")

    # Check Ollama
    import requests
    try:
        r = requests.get(f"{OLLAMA_HOST}/api/tags", timeout=10)
        assert r.status_code == 200
        print("  Ollama: connected")
    except:
        print("  ⚠️ Ollama not reachable. LLM tests will fail.")
        print("     Start with: ollama serve")

    # Check Piper
    if not os.path.exists(PIPER_BINARY):
        print(f"  ⚠️ Piper not found at {PIPER_BINARY}. TTS tests will fail.")

    # Queries for LLM tests (variety)
    queries = [
        "What is the weather like today?",
        "Tell me a fun fact about robots.",
        "What time is it?",
        "How does a computer work?",
        "What is your name?",
        "Tell me a short joke.",
        "What is artificial intelligence?",
        "How far is the moon?",
        "What is the capital of Egypt?",
        "Say something nice.",
    ]

    # --- Run all versions ---
    all_results = []
    for vid in version_ids:
        if vid not in VERSIONS:
            print(f"\n  ⚠️ Skipping unknown version: {vid}")
            continue

        result = run_version(vid, VERSIONS[vid], audio_float, args.trials, queries)
        if result:
            all_results.append(result)

    if not all_results:
        print("\n  No versions completed successfully.")
        sys.exit(1)

    # ==========================================================================
    # FINAL COMPARISON TABLE
    # ==========================================================================
    print("\n")
    print("=" * 90)
    print("  FINAL COMPARISON — ALL VERSIONS")
    print("=" * 90)

    # Header
    print(f"{'Version':<12} {'STT (ms)':<18} {'LLM (ms)':<18} {'TTS (ms)':<18} {'TOTAL (ms)':<18}")
    print("-" * 90)

    for r in all_results:
        stt = f"{r['stt_mean']:.0f} ± {r['stt_std']:.0f}"
        llm = f"{r['llm_mean']:.0f} ± {r['llm_std']:.0f}"
        tts = f"{r['tts_mean']:.0f} ± {r['tts_std']:.0f}"
        total = f"{r['total_mean']:.0f} ± {r['total_std']:.0f}"
        print(f"{r['version']:<12} {stt:<18} {llm:<18} {tts:<18} {total:<18}")

    print("=" * 90)

    # Improvement ratio
    if len(all_results) >= 2:
        first = all_results[0]
        last = all_results[-1]
        if first["total_mean"] > 0 and last["total_mean"] > 0:
            ratio = first["total_mean"] / last["total_mean"]
