def load_audio_as_float(audio_path):
    """Load WAV file and return float32 numpy array at 16kHz"""
    with wave.open(str(audio_path), "rb") as wf:
        n_frames = wf.getnframes()
        sample_rate = wf.getframerate()
        n_channels = wf.getnchannels()
        raw = wf.readframes(n_frames)

    audio = np.frombuffer(raw, dtype=np.int16)
    if n_channels == 2:
        audio = audio[::2]
    audio_float = audio.astype(np.float32) / 32768.0

    # Resample to 16kHz if needed
    if sample_rate != 16000:
        duration = len(audio_float) / sample_rate
        target_len = int(duration * 16000)
        indices = np.linspace(0, len(audio_float) - 1, target_len)
        audio_float = np.interp(indices, np.arange(len(audio_float)), audio_float).astype(np.float32)

    # Normalize
    mx = np.max(np.abs(audio_float))
    if mx > 0:
        audio_float = audio_float / mx * 0.95

    return audio_float


