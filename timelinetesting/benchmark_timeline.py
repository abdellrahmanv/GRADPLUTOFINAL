def warmup_llm(model_name):
    """Warmup Ollama model with a short prompt"""
    import requests
    print(f"    Warming up LLM: {model_name}...")
    t0 = time.perf_counter()
    try:
        resp = requests.post(
            f"{OLLAMA_HOST}/api/generate",
            json={"model": model_name, "prompt": "Hi", "stream": False,
                  "options": {"num_predict": 5}},
            timeout=120,
        )
        elapsed = (time.perf_counter() - t0) * 1000
        if resp.status_code == 200:
            print(f"    LLM warm in {elapsed:.0f}ms")
        else:
            print(f"    ⚠️ LLM warmup returned {resp.status_code}")
    except Exception as e:
        print(f"    ❌ LLM warmup failed: {e}")


def benchmark_llm(user_text, config):
    """
    Run LLM inference with the given config.
    Returns (response_text, total_latency_ms, first_token_ms).
    """
    import requests

    model = config["llm_model"]
    stream = config["llm_stream"]
    max_tokens = config["llm_max_tokens"]
    num_ctx = config["llm_num_ctx"]
    temperature = config["llm_temperature"]

    prompt = f"{SYSTEM_PROMPT}\n\nUser: {user_text}\nPluto:"

    start = time.perf_counter()
    first_token_time = None

    try:
        response = requests.post(
            f"{OLLAMA_HOST}/api/generate",
            json={
                "model": model,
                "prompt": prompt,
                "stream": stream,
                "options": {
                    "num_predict": max_tokens,
                    "temperature": temperature,
                    "top_p": 0.9,
                    "num_ctx": num_ctx,
                },
            },
            timeout=60,
            stream=stream,
        )

        if response.status_code != 200:
            return None, 0, 0

        if stream:
            result = ""
            for line in response.iter_lines():
                if line:
                    data = json.loads(line)
                    token = data.get("response", "")
                    result += token
                    if first_token_time is None and token:
                        first_token_time = time.perf_counter()
                    if data.get("done", False):
                        break
        else:
            data = response.json()
            result = data.get("response", "")
            first_token_time = time.perf_counter()  # No streaming, so first = total

        total_ms = (time.perf_counter() - start) * 1000
        ft_ms = (first_token_time - start) * 1000 if first_token_time else total_ms

        result = result.strip().split("\n")[0].strip()
        if result.startswith('"') and result.endswith('"'):
            result = result[1:-1]

        return result, total_ms, ft_ms

    except Exception as e:
        print(f"    ❌ LLM error: {e}")
        return None, 0, 0


